// Math Toolkit Builder
function double(n) {
    return n * 2;
    }
console.log(double(4)); // Output: 8

function square(n) {
    return n * n;
}
console.log(square(4)); // Output: 16


function isEven(n) {
   return n % 2 === 0;
}
console.log(isEven(7)); // Output: false

function isOdd(n) {
   return n % 2 !== 0;
}
console.log(isOdd(7)); // Output: true


function multiply(a, b) {
   return a * b;
}
console.log(multiply(4, 5)); // Output: 20
